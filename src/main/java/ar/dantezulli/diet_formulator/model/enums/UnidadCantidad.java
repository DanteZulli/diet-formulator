package ar.dantezulli.diet_formulator.model.enums;

/**
 * Unit of measurement for diet item quantities.
 * Unidad de medida para cantidades de ítems de dieta.
 */
public enum UnidadCantidad {

    /** Grams. / Gramos. */
    GRAMOS,

    /** Milligrams. / Miligramos. */
    MILIGRAMOS,

    /** Capsules. / Cápsulas. */
    CAPSULAS,

    /** Units (pieces, tablets, etc.). / Unidades (piezas, tabletas, etc.). */
    UNIDADES
}
